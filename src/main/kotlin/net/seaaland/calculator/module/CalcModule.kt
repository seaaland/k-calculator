package net.seaaland.calculator.module

class CalcModule {

    fun add(one: Int, two: Int): Int {
        return one + two;
    }

    fun subtract(one: Int, two: Int): Int {
        return one - two;
    }

    fun multiply(one: Int, two: Int): Int {
        return one * two;
    }

    fun divide(one: Int, two: Int): Int {
        return one / two;
    }
}